def ensure_settings(scope_id: str) -> Path:
    """Create/refresh a scope's GraphRAG root: settings.yaml + prompts/. Does not touch
    input/ — callers add documents via write_source_input."""
    root = root_for(scope_id)
    (root / "input").mkdir(parents=True, exist_ok=True)
    (root / "prompts").mkdir(parents=True, exist_ok=True)
    (root / "settings.yaml").write_text(_settings_yaml(), encoding="utf-8")

    tmpl = config.GRAPHRAG_TEMPLATE_DIR / "prompts"
    if tmpl.exists():
        for src in tmpl.glob("*.txt"):
            dst = root / "prompts" / src.name
            if not dst.exists():
                dst.write_text(src.read_text(encoding="utf-8"), encoding="utf-8")
    return root


def write_source_input(scope_id: str, source_id: str, text: str) -> None:
    """Write/overwrite this one source's document inside scope_id's input/ dir. Other
    sources' files in the same scope are untouched — this is what lets the global scope
    accumulate documents across many ingests."""
    root = ensure_settings(scope_id)
    (root / "input" / f"{source_id}.txt").write_text(text, encoding="utf-8")


def remove_source_input(scope_id: str, source_id: str) -> None:
    f = root_for(scope_id) / "input" / f"{source_id}.txt"
    if f.exists():
        f.unlink()


def build(scope_id: str) -> None:
    """Build the index in-process (blocking) from whatever is currently in input/."""
    import io
    from contextlib import redirect_stdout, redirect_stderr

    lock = _lock_for(scope_id)
    if not lock.acquire(blocking=False):
        print(f"[graphrag] build already in progress for {scope_id}", file=sys.stderr)
        return
    root = root_for(scope_id)

    with _build_logs_lock:
        _build_logs[scope_id] = []

    class _Tee(io.TextIOBase):
        def __init__(self, real) -> None:
            self._real = real
            self._buf = ""

        def write(self, s: str) -> int:
            self._real.write(s)
            self._buf += s
            while "\n" in self._buf:
                line, self._buf = self._buf.split("\n", 1)
                if line.strip():
                    _append_log(scope_id, line)
            return len(s)

        def flush(self) -> None:
            self._real.flush()
            if self._buf.strip():
                _append_log(scope_id, self._buf)
                self._buf = ""

    try:
        _write_status(root, "building", "Scaffolding GraphRAG root…")
        _append_log(scope_id, "Scaffolding GraphRAG root…")
        ensure_settings(scope_id)
        _write_status(root, "building", "Building knowledge graph (entities, communities, reports)…")
        _append_log(scope_id, "Building knowledge graph — extracting entities, clustering communities, generating reports…")
        print(f"[graphrag] building index for {scope_id} …", file=sys.stderr)
        from graphrag_qa import build_index
        tee_out, tee_err = _Tee(sys.stdout), _Tee(sys.stderr)
        with redirect_stdout(tee_out), redirect_stderr(tee_err):
            build_index(root)
        tee_out.flush(); tee_err.flush()
        _engines.pop(scope_id, None)
        _write_status(root, "ready", "")
        _append_log(scope_id, "Index ready.")
        print(f"[graphrag] index ready for {scope_id}", file=sys.stderr)
    except Exception as e:
        msg = f"{type(e).__name__}: {e}"
        _write_status(root, "failed", msg)
        _append_log(scope_id, f"FAILED: {msg}")
        print(f"[graphrag] build FAILED for {scope_id}: {e}", file=sys.stderr)
    finally:
        lock.release()


def build_async(scope_id: str, force: bool = False) -> bool:
    """Start a background build. Returns True if started.

    force=True is required to pick up newly added/changed input documents on an already
    -healthy root (e.g. every ingest into the global scope), since health() alone can't
    tell that a new source.txt was just dropped into input/.
    """
    if not force and health(scope_id):
        return False
    lock = _lock_for(scope_id)
    if lock.locked():
        return False
    t = threading.Thread(target=build, args=(scope_id,), daemon=True, name=f"graphrag-build-{scope_id}")
    t.start()
    return True


# A single global lock serializes ALL builds across every scope: running graphrag's
# build_index() for two different roots at the same time in this process was observed to
# silently produce an empty output/ for one of the two roots (no exception raised) — some
# state inside the graphrag package isn't safe for concurrent indexing runs.
_global_build_lock = threading.Lock()


def build_sequential_async(scope_ids: list[str], force: bool = False) -> None:
    """Build several scopes one at a time in a single background thread."""
    def run():
        with _global_build_lock:
            for scope_id in scope_ids:
                if not force and health(scope_id):
                    continue
                build(scope_id)
    threading.Thread(target=run, daemon=True, name="graphrag-build-sequential").start()


def get_engine(scope_id: str):
    if not health(scope_id):
        return None
    eng = _engines.get(scope_id)
    if eng is None:
        from graphrag_qa import GraphRAGQA
        eng = GraphRAGQA(root=root_for(scope_id))
        _engines[scope_id] = eng
    return eng


def list_communities(scope_id: str, level: int = 0) -> list[dict[str, Any]] | None:
    eng = get_engine(scope_id)
    if eng is None:
        return None
    import pandas as pd

    communities = eng.communities[eng.communities["level"] == level]
    reports = eng.community_reports.set_index("community")
    out = []
    for row in communities.itertuples():
        report = reports.loc[row.community] if row.community in reports.index else None
        rank = report["rank"] if report is not None else None
        out.append({
            "id": int(row.community), "level": int(row.level), "parent": int(row.parent),
            "children": [int(c) for c in row.children], "title": str(row.title),
            "size": int(row.size), "rank": None if rank is None or pd.isna(rank) else float(rank),
            "summary": str(report["summary"]) if report is not None else "",
        })
    out.sort(key=lambda c: c["size"], reverse=True)
    return out


def get_community_detail(scope_id: str, community_id: int) -> dict[str, Any] | None:
    eng = get_engine(scope_id)
    if eng is None:
        return None
    import pandas as pd

    rows = eng.communities[eng.communities["community"] == community_id]
    if rows.empty:
        return None
    row = rows.iloc[0]
    reports = eng.community_reports[eng.community_reports["community"] == community_id]
    report = reports.iloc[0] if not reports.empty else None

    entity_ids = set(row["entity_ids"].tolist())
    relationship_ids = set(row["relationship_ids"].tolist())
    entities = eng.entities[eng.entities["id"].isin(entity_ids)]
    nodes = [{"id": str(e.title), "label": str(e.title), "type": str(e.type).lower(),
              "description": str(e.description), "frequency": int(e.frequency), "degree": int(e.degree)}
             for e in entities.itertuples()]
    relationships = eng.relationships[eng.relationships["id"].isin(relationship_ids)]
    edges = [{"source": str(r.source), "target": str(r.target), "relation": str(r.description), "weight": float(r.weight)}
             for r in relationships.itertuples()]
    rank = report["rank"] if report is not None else None
    return {
        "community": {
            "id": int(row["community"]), "level": int(row["level"]), "title": str(row["title"]),
            "size": int(row["size"]), "summary": str(report["summary"]) if report is not None else "",
            "full_content": str(report["full_content"]) if report is not None else "",
            "rank": None if rank is None or pd.isna(rank) else float(rank),
        },
        "nodes": nodes, "edges": edges,
    }
