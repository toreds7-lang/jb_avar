"""Per-scope Microsoft GraphRAG lifecycle: scaffold, build, status, engine cache.

Ported from paper_read_project_pdf/graphrag_manager.py and restructured for two kinds of
scope that share this same code path:
  * a per-source scope (scope_id == source_id) — exactly one input document.
  * the global cumulative scope (scope_id == config.GLOBAL_SCOPE_ID) — one input file per
    ingested source, accumulated over time (write_source_input is called once per source,
    never overwriting another source's file), so re-ingesting a new source and rebuilding
    folds it into the same growing index.

Builds are heavy (minutes, many LLM calls). serve.py kicks one off in the background after
ingest; chat degrades to vector RAG until the index is ready.
"""
from __future__ import annotations

import json
import sys
import threading
import time
from pathlib import Path
from typing import Any

import config

_REQUIRED = ("entities", "communities", "community_reports", "text_units", "relationships")
_STATUS_FILE = "status.json"

_engines: dict[str, Any] = {}
_locks: dict[str, threading.Lock] = {}
_locks_guard = threading.Lock()

_LOG_MAXLEN = 200
_build_logs: dict[str, list[str]] = {}
_build_logs_lock = threading.Lock()


def root_for(scope_id: str) -> Path:
    return config.DATA_DIR / scope_id / "graphrag"


def _lock_for(scope_id: str) -> threading.Lock:
    with _locks_guard:
        return _locks.setdefault(scope_id, threading.Lock())


def _write_status(root: Path, state: str, message: str = "") -> None:
    root.mkdir(parents=True, exist_ok=True)
    (root / _STATUS_FILE).write_text(
        json.dumps({"state": state, "message": message, "updated_at": time.time()}), encoding="utf-8"
    )


def get_logs(scope_id: str) -> list[str]:
    with _build_logs_lock:
        return list(_build_logs.get(scope_id, []))


def _append_log(scope_id: str, line: str) -> None:
    with _build_logs_lock:
        buf = _build_logs.setdefault(scope_id, [])
        buf.append(line)
        if len(buf) > _LOG_MAXLEN:
            del buf[: len(buf) - _LOG_MAXLEN]


def read_status(scope_id: str) -> dict[str, Any]:
    root = root_for(scope_id)
    if _lock_for(scope_id).locked():
        return {"state": "building", "message": "Rebuilding…"}
    if health(scope_id):
        return {"state": "ready", "message": ""}
    f = root / _STATUS_FILE
    if f.exists():
        try:
            return json.loads(f.read_text(encoding="utf-8"))
        except Exception:
            pass
    return {"state": "idle", "message": ""}


def health(scope_id: str) -> bool:
    out = root_for(scope_id) / "output"
    return all((out / f"{n}.parquet").exists() for n in _REQUIRED)


